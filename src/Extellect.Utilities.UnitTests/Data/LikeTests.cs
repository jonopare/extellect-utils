        [Fact]
        public void Ctor_PatternIsNull_Throws()
        {
            Assert.Throws<ArgumentNullException>(() => new Like(null));
        }

        [Fact]
        public void IsMatch_FPercent()
        {
            Assert.True(new Like("%u%").IsMatch("Funky"));
            Assert.True(new Like("%y").IsMatch("Funky"));
            Assert.True(new Like("F%").IsMatch("Funky"));
        }

        [Fact]
        public void IsMatch_WorkInProgress()
        {
            Assert.False(new Like("_").IsMatch("ab"));
        }

        [Fact]
        public void IsMatch_WithoutWildCards()
        {
            var like = new Like("a");
            Assert.True(like.IsMatch("a"));
            Assert.False(like.IsMatch("b"));
            Assert.False(like.IsMatch(""));
            Assert.False(like.IsMatch("aa"));
        }

        [Fact]
        public void IsMatch_SingleWildCard()
        {
            var like = new Like("_");
            Assert.True(like.IsMatch("_"));
            Assert.True(like.IsMatch("b"));
            Assert.False(like.IsMatch(""));
            Assert.False(like.IsMatch("aa"));
        }

        [Fact]
        public void IsMatch_Assorted()
        {
            var like = new Like("brown%dog");
            Assert.True(like.IsMatch("brown dogdog"));
            Assert.False(like.IsMatch("brown dogdogcat"));
        }

